<h2>Что фиксировать после совещания с подрядчиком</h2>

<p>После встречи или обхода важно не ограничиваться устной договоренностью. Нужно зафиксировать, какие работы принимаются, какие остаются с замечаниями, кто отвечает за устранение, какой срок согласован и какие документы потребуются для закрытия этапа. Если были изменения по объему или сроку, они тоже должны попасть в систему.</p>

<p>Такой короткий протокол защищает команду от повторного обсуждения одних и тех же вопросов. Через неделю можно открыть карточку подрядчика и увидеть историю решений, а не восстанавливать ее по сообщениям и памяти участников.</p>

<p>Особенно полезно фиксировать не только финальное решение, но и причину. Если срок перенесли из-за отсутствия фронта работ, это одна управленческая проблема. Если подрядчик не вывел людей или не закрыл замечания, это уже другая история и другой разговор.</p>

